package ejercicioC1;

public class Punto {
	
	private int X;
	private int Y;
	
	public Punto(int x, int y) {
		this.X = x;
		this.Y = y;
	}
	
	public int getX () {
		return X;
	}
	
	public int getY () {
		return Y;
	}
	
	public void setX(int X) {
		this.X = X;
	}
	
	public void setY(int Y) {
		this.Y = Y;
	}
}
